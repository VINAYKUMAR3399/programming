public class OddorEven{  
		public static void main(String args[]){  
			int num[]={2,4,5,8,9,1};  
			
				System.out.println("Odd Numbers are ");
					for(int i=0;i<num.length;i++){  
						if(num[i]%2!=0){  
							System.out.println(num[i]);  
				}  
	}  
				System.out.println("Even Numbers are:");  
						for(int i=0;i<num.length;i++){  
							if(num[i]%2==0){  
									System.out.println(num[i]);  
		}  
	}  
	}
}  